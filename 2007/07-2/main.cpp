/*
找出这样的数：本身是三位的完全平方数n（100<=n<1000）而且有
两位上的数字相同，如484 
*/
#include <iostream>
using namespace std;

int main() {
	int n;
	for(int i=10;i<32;i++){  //10的平方是100, 32的平方是1024，则10~32之间的数字的平方均为3位数
		n=i*i;  //n为i的平方，为三位数
		int m=n/10;  //m为n的前两位
		if(n/100==n%10 || n/100==m%10 ||n%10==m%10)  //判断n这个三位数中是否存在两位数相同，若相同，则输出n
		cout<<n<<"  ";
	}
	return 0;
}